function buildPostgresSeed(): string {
  const lines: string[] = [];
  lines.push("-- Auto-generated seed data for PostgreSQL databases");
  lines.push("-- Generated by scripts/build-seed-sql.ts");
  lines.push("");

  const supplierCountries = assignCountries();
  const factoryNames = content.factoryNames.length >= SUPPLIER_COUNT
    ? content.factoryNames
    : [...content.factoryNames, ...Array.from({ length: SUPPLIER_COUNT - content.factoryNames.length }, (_, i) => `Factory ${i + 201}`)];

  // ═══════════════════════════════════════════════════════════
  // wc_global: Workers (mdl_participant)
  // ═══════════════════════════════════════════════════════════
  lines.push("\\connect wc_global");
  lines.push("");
  lines.push("BEGIN;");
  lines.push("");

  // ── mdl_participant (workers) ──
  lines.push("-- Workers (mdl_participant) in wc_global");
  const firstNames = content.workerFirstNames.length > 0 ? content.workerFirstNames : Array.from({ length: 100 }, (_, i) => `Worker${i}`);
  const lastNames = content.workerLastNames.length > 0 ? content.workerLastNames : Array.from({ length: 50 }, (_, i) => `Last${i}`);

  // Power-law distribution of workers across suppliers
  const workerDistrib = paretoDistribution(WORKER_COUNT, SUPPLIER_COUNT, 0.7);

  let workerId = 1;
  const WORKER_BATCH = 500;
  let batchValues: string[] = [];

  for (let s = 0; s < SUPPLIER_COUNT; s++) {
    const supplierId = s + 1;
    const count = Math.max(10, workerDistrib[s]);

    for (let w = 0; w < count && workerId <= WORKER_COUNT; w++) {
      const fn = esc(pick(firstNames));
      const ln = esc(pick(lastNames));
      const gender = seededRandom() < 0.6 ? 1 : 2;
      const startDate = isoDate(randomDate(1095));
      const identifier = `W${String(workerId).padStart(6, "0")}`;
      const phone = `+${randInt(1, 99)}${String(randInt(100000000, 999999999))}`;

      batchValues.push(
        `(${workerId}, ${supplierId}, '${fn}', '${ln}', '${fn} ${ln}', '${fn} ${ln}', '${identifier}', true, false, false, ${gender}, '${startDate}', '${phone}')`,
      );

      if (batchValues.length >= WORKER_BATCH) {
        lines.push(
          `INSERT INTO mdl_participant (id, client_id, first_name, last_name, full_name, clean_full_name, identifier, is_active, is_transfer, is_deleted, gender_id, worker_start_date, phone_number) VALUES\n${batchValues.join(",\n")};`,
        );
        batchValues = [];
      }
      workerId++;
    }
  }
  if (batchValues.length > 0) {
    lines.push(
      `INSERT INTO mdl_participant (id, client_id, first_name, last_name, full_name, clean_full_name, identifier, is_active, is_transfer, is_deleted, gender_id, worker_start_date, phone_number) VALUES\n${batchValues.join(",\n")};`,
    );
  }
  lines.push(`SELECT setval('mdl_participant_id_seq', ${workerId - 1}, true);`);
  lines.push("");
  lines.push("COMMIT;");
  lines.push("");

  // ═══════════════════════════════════════════════════════════
  // wovo_new: Suppliers, Surveys, Responses
  // ═══════════════════════════════════════════════════════════
  lines.push("\\connect wovo_new");
  lines.push("");
  lines.push("BEGIN;");
  lines.push("");

  // ── Prerequisite: supported languages (FK target for survey responses) ──
  lines.push("-- Supported languages (FK prerequisite)");
  lines.push(`INSERT INTO translations_mdlsupportedlanguages (id, name, language_code, is_active, is_ui_language, is_translated, resource_version, is_app_supported, wovo_language_id, created_date, modified_date) VALUES`);
  lines.push(`  (1, 'English', 'en', true, true, true, 1.0, true, 1, NOW(), NOW()),`);
  lines.push(`  (2, 'Vietnamese', 'vi', true, true, true, 1.0, true, 2, NOW(), NOW()),`);
  lines.push(`  (3, 'Bengali', 'bn', true, true, true, 1.0, true, 3, NOW(), NOW()),`);
  lines.push(`  (4, 'Khmer', 'km', true, true, true, 1.0, true, 4, NOW(), NOW()),`);
  lines.push(`  (5, 'Chinese', 'zh', true, true, true, 1.0, true, 5, NOW(), NOW()),`);
  lines.push(`  (6, 'Hindi', 'hi', true, true, true, 1.0, true, 6, NOW(), NOW()),`);
  lines.push(`  (7, 'Indonesian', 'id', true, true, true, 1.0, true, 7, NOW(), NOW()),`);
  lines.push(`  (8, 'Thai', 'th', true, true, true, 1.0, true, 8, NOW(), NOW()),`);
  lines.push(`  (9, 'Burmese', 'my', true, true, true, 1.0, true, 9, NOW(), NOW()),`);
  lines.push(`  (10, 'Amharic', 'am', true, true, true, 1.0, true, 10, NOW(), NOW());`);
  lines.push(`SELECT setval(pg_get_serial_sequence('translations_mdlsupportedlanguages', 'id'), 10, true);`);
  lines.push("");

  // ── clients_clientinfo ──
  lines.push("-- Suppliers (clients_clientinfo)");
  for (let i = 1; i <= SUPPLIER_COUNT; i++) {
    const name = esc(factoryNames[i - 1]);
    const country = supplierCountries[i - 1];
    const created = isoDate(randomDate(730));
    lines.push(
      `INSERT INTO clients_clientinfo (id, name, client_key, client_id, country, is_active, is_deleted, is_anonymous, allow_connect_raw_data_download, allow_survey_raw_data_download, allow_questionnaire_raw_data_download, created_at, created_date) VALUES (${i}, '${name}', ${i}, 'supplier-${String(i).padStart(3, "0")}', '${country}', true, false, false, false, false, false, '${created}', '${created}');`,
    );
  }
  lines.push(`SELECT setval('clients_clientinfo_id_seq', ${SUPPLIER_COUNT}, true);`);
  lines.push("");

  // ── survey_mdlsurveyquestiontype ──
  lines.push("-- Survey question types");
  const qtypes = [
    { name: "Scored", question_type: 1, dic_key: "scored" },
    { name: "Single Select", question_type: 2, dic_key: "single_select" },
    { name: "Multi Select", question_type: 3, dic_key: "multi_select" },
    { name: "Free Text", question_type: 4, dic_key: "free_text" },
  ];
  const qtypeUuids: string[] = [];
  for (const qt of qtypes) {
    const uuid = randomUUID();
    qtypeUuids.push(uuid);
    lines.push(
      `INSERT INTO survey_mdlsurveyquestiontype (id, name, question_type, is_active, dic_key) VALUES ('${uuid}', '${qt.name}', ${qt.question_type}, true, '${qt.dic_key}');`,
    );
  }
  lines.push("");

  // ── surveys + questions + options + responses ──
  lines.push("-- Surveys, questions, options, and responses");

  const surveyQuestions = content.surveyQuestionTitles.length > 0
    ? content.surveyQuestionTitles
    : Array.from({ length: 50 }, (_, i) => `Survey Question ${i + 1}`);

  const optionSets = content.surveyScoreOptionSets.length > 0
    ? content.surveyScoreOptionSets
    : [{ options: [{ label: "Good", weight: 100 }, { label: "Fair", weight: 50 }, { label: "Poor", weight: 25 }] }];

  const freeTextResponses = content.surveyFreeTextResponses.length > 0
    ? content.surveyFreeTextResponses
    : ["No comment.", "Everything is fine.", "Need improvement."];

  let surveyQuestionJunctionId = 1;
  let questionOptionJunctionId = 1;
  let userResponseCount = 0;
  let questionResponseCount = 0;

  // Batch collections for bulk insert
  const surveyInserts: string[] = [];
  const questionInserts: string[] = [];
  const optionInserts: string[] = [];
  const sqJunctionInserts: string[] = [];
  const qoJunctionInserts: string[] = [];
  const userResponseInserts: string[] = [];
  const questionResponseInserts: string[] = [];

  for (let s = 0; s < SURVEY_COUNT; s++) {
    const surveyId = randomUUID();
    const supplierId = (s % SUPPLIER_COUNT) + 1;
    const statusRoll = seededRandom();
    const status = statusRoll < 0.3 ? 1 : statusRoll < 0.9 ? 2 : 0; // active, closed, draft
    const fromDate = randomDate(730);
    const toDate = new Date(fromDate);
    toDate.setDate(toDate.getDate() + randInt(14, 60));
    const surveyName = esc(pick(surveyQuestions) + ` - ${fromDate.getFullYear()} Q${Math.ceil((fromDate.getMonth() + 1) / 3)}`);

    surveyInserts.push(
      `('${surveyId}', ${supplierId}, '${surveyName}', '${isoDate(fromDate)}', '${isoDate(toDate)}', ${status}, 1, ${s + 1}, false, false, false, false, false, '${isoDate(fromDate)}')`,
    );

    // Questions for this survey
    const questionIds: string[] = [];
    const questionOptionIds: Map<string, string[]> = new Map();

    for (let q = 0; q < QUESTIONS_PER_SURVEY; q++) {
      const questionId = randomUUID();
      questionIds.push(questionId);

      // 60% scored, 10% single, 10% multi, 20% free text
      const typeRoll = seededRandom();
      let typeIdx: number;
      if (typeRoll < 0.6) typeIdx = 0;       // scored
      else if (typeRoll < 0.7) typeIdx = 1;   // single
      else if (typeRoll < 0.8) typeIdx = 2;   // multi
      else typeIdx = 3;                        // free text

      const questionTitle = esc(surveyQuestions[(s * QUESTIONS_PER_SURVEY + q) % surveyQuestions.length]);

      questionInserts.push(
        `('${questionId}', ${supplierId}, '${questionTitle}', true, true, false, true, true, false, ${q + 1}, '${qtypeUuids[typeIdx]}', '${isoDate(fromDate)}')`,
      );

      sqJunctionInserts.push(
        `(${surveyQuestionJunctionId++}, '${surveyId}', '${questionId}')`,
      );

      // Options for non-free-text questions
      const optIds: string[] = [];
      if (typeIdx < 3) {
        const optSet = optionSets[q % optionSets.length];
        for (const opt of optSet.options) {
          const optId = randomUUID();
          optIds.push(optId);
          const weight = typeIdx === 0 ? String(opt.weight) : "0";
          optionInserts.push(
            `('${optId}', ${supplierId}, '${esc(opt.label)}', '${weight}', false, '', '${isoDate(fromDate)}')`,
          );
          qoJunctionInserts.push(
            `(${questionOptionJunctionId++}, '${questionId}', '${optId}')`,
          );
        }
      }
      questionOptionIds.set(questionId, optIds);
    }

    // User responses (50-150 per survey for completed/active surveys)
    if (status > 0) { // active or closed
      const responseCount = randInt(50, 150);
      for (let r = 0; r < responseCount && userResponseCount < 50000; r++) {
        const userId = randInt(1, WORKER_COUNT);
        const urId = randomUUID();
        const gender = seededRandom() < 0.6 ? 1 : 2;
        const age = randInt(18, 55);
        const tenure = randInt(1, 120); // months
        const ageLabel = age < 25 ? "18-24" : age < 35 ? "25-34" : age < 45 ? "35-44" : "45+";
        const tenureLabel = tenure < 12 ? "< 1 year" : tenure < 36 ? "1-3 years" : "3+ years";
        const country = supplierCountries[supplierId - 1];

        userResponseInserts.push(
          `('${urId}', ${supplierId}, ${userId}, 1, 1, 1, 0, '${country}', ${gender}, ${age}, ${tenure}, '${ageLabel}', '${tenureLabel}', 'W${String(userId).padStart(6, "0")}', '${esc(country)}', 0, 0, 1, '${surveyId}', '${isoDate(fromDate)}')`,
        );
        userResponseCount++;

        // Answer each question
        for (let q = 0; q < questionIds.length && questionResponseCount < 250000; q++) {
          if (seededRandom() < 0.1) continue; // 10% skip rate

          const qId = questionIds[q];
          const optIds = questionOptionIds.get(qId) || [];
          const qrId = randomUUID();
          let textResponse: string;
          let optionId: string;

          if (optIds.length > 0) {
            // Select an option (weighted toward higher scores for realism)
            const idx = Math.min(optIds.length - 1, Math.floor(Math.pow(seededRandom(), 1.5) * optIds.length));
            optionId = optIds[idx];
            textResponse = "";
          } else {
            // Free text
            optionId = "";
            textResponse = esc(pick(freeTextResponses));
          }

          questionResponseInserts.push(
            `('${qrId}', ${supplierId}, '${esc(textResponse)}', ${optionId ? `'${optionId}'` : "NULL"}, '${surveyId}', '${qId}', '${urId}', '${isoDate(fromDate)}')`,
          );
          questionResponseCount++;
        }
      }
    }
  }

  // Write surveys
  const BATCH = 200;
  for (let i = 0; i < surveyInserts.length; i += BATCH) {
    lines.push(
      `INSERT INTO survey_mdlsurvey (id, client_id, name, from_date, to_date, status, source, sequence_id, is_locked, is_anonymous, is_clients_disabled, one_response_per_device, is_randomize, created_date) VALUES\n${surveyInserts.slice(i, i + BATCH).join(",\n")};`,
    );
  }
  lines.push("");

  // Write questions
  for (let i = 0; i < questionInserts.length; i += BATCH) {
    lines.push(
      `INSERT INTO survey_mdlsurveyquestions (id, client_id, title, is_active, is_accessible, is_deleted, is_mandatory, is_locked, is_emoji_enabled, sequence_id, q_type_id, created_date) VALUES\n${questionInserts.slice(i, i + BATCH).join(",\n")};`,
    );
  }
  lines.push("");

  // Write options
  for (let i = 0; i < optionInserts.length; i += BATCH) {
    lines.push(
      `INSERT INTO survey_mdlsurveyquestionoptions (id, client_id, label, weight, is_cna, icon_class, created_date) VALUES\n${optionInserts.slice(i, i + BATCH).join(",\n")};`,
    );
  }
  lines.push("");

  // Write survey-question junction
  for (let i = 0; i < sqJunctionInserts.length; i += BATCH) {
    lines.push(
      `INSERT INTO survey_mdlsurvey_questions (id, mdlsurvey_id, mdlsurveyquestions_id) VALUES\n${sqJunctionInserts.slice(i, i + BATCH).join(",\n")};`,
    );
  }
  lines.push("");

  // Write question-option junction
  for (let i = 0; i < qoJunctionInserts.length; i += BATCH) {
    lines.push(
      `INSERT INTO survey_mdlsurveyquestions_question_options (id, mdlsurveyquestions_id, mdlsurveyquestionoptions_id) VALUES\n${qoJunctionInserts.slice(i, i + BATCH).join(",\n")};`,
    );
  }
  lines.push("");

  // Write user responses
  for (let i = 0; i < userResponseInserts.length; i += BATCH) {
    lines.push(
      `INSERT INTO survey_mdlsurveyuserresponses (id, client_id, user_id, is_completed, response_type, response_source, randomise_status, country, gender, age, tenure, age_label, tenure_label, identifier, dept_name, smart_phone, job_role, language_id, survey_id_id, created_date) VALUES\n${userResponseInserts.slice(i, i + BATCH).join(",\n")};`,
    );
  }
  lines.push("");

  // Write question responses
  for (let i = 0; i < questionResponseInserts.length; i += BATCH) {
    lines.push(
      `INSERT INTO survey_mdlsurveyquestionresponses (id, client_id, text_response, question_option_id, survey_id, survey_question_id, survey_user_response_id, created_date) VALUES\n${questionResponseInserts.slice(i, i + BATCH).join(",\n")};`,
    );
  }
  lines.push("");

  // Reset sequences
  lines.push(`SELECT setval('survey_mdlsurvey_questions_id_seq', ${surveyQuestionJunctionId}, true);`);
  lines.push(`SELECT setval('survey_mdlsurveyquestions_question_options_id_seq', ${questionOptionJunctionId}, true);`);
  lines.push("");

  lines.push("COMMIT;");
  lines.push("");
  lines.push(`-- Summary: ${SUPPLIER_COUNT} suppliers, ${workerId - 1} workers, ${SURVEY_COUNT} surveys`);
  lines.push(`-- ${questionInserts.length} questions, ${optionInserts.length} options`);
  lines.push(`-- ${userResponseCount} user responses, ${questionResponseCount} question responses`);

  return lines.join("\n");
}

