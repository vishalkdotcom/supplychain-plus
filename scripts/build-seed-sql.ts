function buildMySQLSeed(): string {
  const lines: string[] = [];
  lines.push("-- Auto-generated seed data for iomadprod (Moodle)");
  lines.push("-- Generated by scripts/build-seed-sql.ts");
  lines.push("");
  lines.push("USE iomadprod;");
  lines.push("");

  const allCourses = Object.entries(content.coursesByCategory);

  // ── Course categories ──
  lines.push("-- Course categories");
  for (let i = 0; i < allCourses.length; i++) {
    const [catName] = allCourses[i];
    lines.push(
      `INSERT INTO mdl_course_categories (id, name, sortorder) VALUES (${i + 1}, '${esc(catName)}', ${(i + 1) * 10});`,
    );
  }
  lines.push("");

  // ── Courses ──
  lines.push("-- Courses");
  let courseId = 2; // Moodle reserves id=1 for site course
  const courseIds: number[] = [];

  for (let catIdx = 0; catIdx < allCourses.length; catIdx++) {
    const [, courses] = allCourses[catIdx];
    for (const course of courses) {
      const name = esc(course.name);
      const summary = esc(course.summary);
      const shortname = esc(course.name.substring(0, 30));
      const created = Math.floor(randomDate(730).getTime() / 1000);

      lines.push(
        `INSERT INTO mdl_course (id, category, fullname, shortname, summary, format, visible, enablecompletion, timecreated, timemodified) VALUES (${courseId}, ${catIdx + 1}, '${name}', '${shortname}', '${summary}', 'topics', 1, 1, ${created}, ${created});`,
      );
      courseIds.push(courseId);
      courseId++;
    }
  }
  lines.push("");

  // ── Enrollment methods ──
  lines.push("-- Enrollment methods (one manual per course)");
  let enrolId = 1;
  for (const cId of courseIds) {
    lines.push(
      `INSERT INTO mdl_enrol (id, enrol, status, courseid, timecreated, timemodified) VALUES (${enrolId}, 'manual', 0, ${cId}, UNIX_TIMESTAMP(), UNIX_TIMESTAMP());`,
    );
    enrolId++;
  }
  lines.push("");

  // ── User enrollments ──
  lines.push("-- User enrollments");
  const enrollValues: string[] = [];
  let enrollId = 1;
  const usedPairs = new Set<string>();

  for (let i = 0; i < ENROLLMENT_COUNT; i++) {
    const enrolMethodId = randInt(1, courseIds.length);
    const userId = randInt(1, 5000);
    const key = `${enrolMethodId}:${userId}`;
    if (usedPairs.has(key)) continue;
    usedPairs.add(key);

    const timeStart = Math.floor(randomDate(730).getTime() / 1000);

    enrollValues.push(
      `(${enrollId}, 0, ${enrolMethodId}, ${userId}, ${timeStart}, 2147483647, 0, ${timeStart}, ${timeStart})`,
    );
    enrollId++;

    if (enrollValues.length >= 500) {
      lines.push(
        `INSERT INTO mdl_user_enrolments (id, status, enrolid, userid, timestart, timeend, modifierid, timecreated, timemodified) VALUES\n${enrollValues.join(",\n")};`,
      );
      enrollValues.length = 0;
    }
  }
  if (enrollValues.length > 0) {
    lines.push(
      `INSERT INTO mdl_user_enrolments (id, status, enrolid, userid, timestart, timeend, modifierid, timecreated, timemodified) VALUES\n${enrollValues.join(",\n")};`,
    );
  }
  lines.push("");

  // ── Course completions (~60% rate, varying by "supplier") ──
  lines.push("-- Course completions");
  const completionValues: string[] = [];
  let completionId = 1;
  const usedCompletions = new Set<string>();

  // Varying completion rate per course (20% - 90%)
  const courseCompletionRates = courseIds.map(() => 0.2 + seededRandom() * 0.7);

  for (let i = 0; i < ENROLLMENT_COUNT * 0.8; i++) {
    const courseIdx = randInt(0, courseIds.length - 1);
    const courseIdVal = courseIds[courseIdx];
    const userId = randInt(1, 5000);
    const key = `${userId}:${courseIdVal}`;
    if (usedCompletions.has(key)) continue;

    // Apply per-course completion rate
    if (seededRandom() > courseCompletionRates[courseIdx]) continue;

    usedCompletions.add(key);
    const enrolled = Math.floor(randomDate(730).getTime() / 1000);
    const started = enrolled + randInt(0, 7) * 86400;
    const completed = started + randInt(1, 90) * 86400;

    completionValues.push(
      `(${completionId}, ${userId}, ${courseIdVal}, ${enrolled}, ${started}, ${completed}, 0)`,
    );
    completionId++;

    if (completionValues.length >= 500) {
      lines.push(
        `INSERT INTO mdl_course_completions (id, userid, course, timeenrolled, timestarted, timecompleted, reaggregate) VALUES\n${completionValues.join(",\n")};`,
      );
      completionValues.length = 0;
    }
  }
  if (completionValues.length > 0) {
    lines.push(
      `INSERT INTO mdl_course_completions (id, userid, course, timeenrolled, timestarted, timecompleted, reaggregate) VALUES\n${completionValues.join(",\n")};`,
    );
  }
  lines.push("");
  lines.push(`-- Summary: ${courseIds.length} courses, ${enrollId - 1} enrollments, ${completionId - 1} completions`);

  return lines.join("\n");
}

