// Deterministic seeded random
let _seed = 42;
function seededRandom(): number {
  _seed = (_seed * 16807 + 0) % 2147483647;
  return (_seed - 1) / 2147483646;
}
function randInt(min: number, max: number): number {
  return Math.floor(seededRandom() * (max - min + 1)) + min;
}
function pick<T>(arr: T[]): T {
  return arr[Math.floor(seededRandom() * arr.length)];
}
function esc(s: string): string {
  return s.replace(/'/g, "''");
}

function isoDate(d: Date): string {
  return d.toISOString().slice(0, 19).replace("T", " ");
}

function randomDate(daysBack: number): Date {
  const d = new Date();
  d.setDate(d.getDate() - randInt(0, daysBack));
  d.setHours(randInt(0, 23), randInt(0, 59), randInt(0, 59));
  return d;
}

// Distribute N items with Pareto-like distribution across M buckets
function paretoDistribution(totalItems: number, buckets: number, skew: number = 0.8): number[] {
  const result = new Array(buckets).fill(0);
  for (let i = 0; i < totalItems; i++) {
    // Power-law: early buckets get more items
    const r = seededRandom();
    const bucket = Math.floor(Math.pow(r, 1 / (1 - skew)) * buckets) % buckets;
    result[bucket]++;
  }
  return result;
}

// Assign countries to suppliers using weights
function assignCountries(): string[] {
  const countries: string[] = [];
  for (const c of COUNTRIES) {
    const count = Math.round((c.weight / 100) * SUPPLIER_COUNT);
    for (let i = 0; i < count; i++) countries.push(c.name);
  }
  while (countries.length < SUPPLIER_COUNT) countries.push(pick(COUNTRIES).name);
  while (countries.length > SUPPLIER_COUNT) countries.pop();
  // Shuffle
  return countries.sort(() => seededRandom() - 0.5);
}

